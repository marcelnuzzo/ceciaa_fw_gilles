<h1>Comment enregistrer un nouveau client ?</h1>
<p>Aprés s'être authentifié en renseignant son mail et son mot de passe,
vous pourrez accéder aux différents formulaires.</p>
<p>Il y a 2 types de profil : le profil administrateur, qui peut modifier les 3 tables (client, chargé client et produit)
 et le profil chargé client (gestion) qui aura des droits plus restreints.</p>
<p>Dans le menu de navigation, il y a 3 boutons pour modifier possiblement les tables.</p>
<p>Un bouton permettant d'inscrire un nouveau chargé client.</p>
<p>Un bouton de déconnexion.</p>
<p>Le nom et le prénom du chargé client connecté.</p>